package com.rays.basic;

public class Swaping {
public static void main(String[] args) {
	int a= 45;
	int b= 50;
	int c=0;
	System.out.println("before Swaping");
	System.out.println("a="+a);
	System.out.println("b="+b);
	System.out.println("-------------------");
	c=a;
	a=b;
	b=c;
	System.out.println("after Swping");
	System.out.println("a="+a);
	System.out.println("b="+b);
}
}
